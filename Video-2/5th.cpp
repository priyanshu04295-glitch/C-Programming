#include<iostream>
using namespace std;
int main()
{
    int a=10,b=5;
    cout<<"Sum = "<<(a+b)<<endl;
    cout<<"Difference = "<<(a-b)<<endl;
    cout<<"Product = "<<(a*b)<<endl;
    cout<<"Quotient = "<<(a/b)<<endl;
    cout<<"Remainder = "<<(a%b)<<endl;
    cout<<((float)5/2)<<endl;
    cout<<(3<5)<<endl;
    cout<<(3>5)<<endl;
    cout<<(3==3)<<endl;
    cout<<(3==5)<<endl;
    cout<<(3!=5)<<endl;
    cout<<(3!=3)<<endl;
    cout<<!(3>1)<<endl;
    cout<<((3>1)||(3<2))<<endl;
    cout<<((3>1)&&(3<2))<<endl;
    return 0;
}
